#include <unistd.h>
void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n >= 10)
		ft_putnbr(n / 10);
	ft_putchar((n % 10) + '0');
}

int	ft_recursive_factorial(int nb)
{
	if (nb < 0)
	{
		return (0);
	}
	if (nb == 0 || nb == 1)
	{
		return (1);
	}
	ft_putnbr(nb);
	ft_putchar('\n');
	return (nb / ft_recursive_factorial(nb - 1));
}

int	main(void)
{
	ft_putnbr(ft_recursive_factorial(10));
	ft_putchar('\n');
	return (0);
}
