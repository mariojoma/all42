#include <unistd.h>
void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n >= 10)
		ft_putnbr(n / 10);
	ft_putchar((n % 10) + '0');
}

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	return i;
}

int	main(int argc, char **argv)
{
	if (argc != 2)
	{
		ft_putchar('e');
		return 0;
	}
	ft_putnbr(ft_strlen(argv[1]));
	ft_putchar('o');
	return 0;
}
